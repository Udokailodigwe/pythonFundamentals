# Printing out multiples strings
print("what", "a", "day")
print("1","2","3", 4,5)

# Printing out multiple string with a seperator
print("what", "a", "day", sep="-")
print("1","2","3", 4,5, sep = "<")

# Printing out multiple string with a seperator and ending
print("what", "a", "day", sep="-", end="!\n")
print("1","2","3", 4,5, sep = "<")




